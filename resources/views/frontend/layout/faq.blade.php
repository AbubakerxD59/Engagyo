                    <h2>How can I preview posts from calendar?</h2>
                    <i class='bx bx-plus'></i>
                </div>
                <div class="faq-content">
                    <p>
                        There are two ways you can preview posts on the calendar:
                        <br>
                        1. By clicking on a specific media and selecting the small eye icon at the bottom.
                        <br>
                        2. Select the daily view, select the desired date, and {{ env('APP_NAME', 'Engagyo') }} will allow you to preview posts in
                        real-time on the right side of the screen.
                    </p>
                </div>
            </div>
            <div class="faq-item">
                <div class="faq-link">
                    <h2>Can I also view post that I manually share?</h2>
                    <i class='bx bx-plus'></i>
                </div>
                <div class="faq-content">
                    <p>
                        Yes. {{ env('APP_NAME', 'Engagyo') }} allows you to turn on the synchronization for all posts that you manually share,
                        too. These settings are account specific, meaning that you get to pick which manual posts you
                        want to see on the calendar and which do not.
                    </p>
                </div>
            </div>
            <div class="faq-item">
                <div class="faq-link">
                    <h2>I can't find my Drafts on the virtual calendar. What should I do?</h2>
                    <i class='bx bx-plus'></i>
                </div>
                <div class="faq-content">
                    <p>
                        If you can't find Draft posts on the calendar, that means that you haven't added a date and time
                        to that post before saving it as a Draft, and also, you haven't specified the social account t
                        belongs to. Therefore, it can't be located anywhere.
                    </p>
                    <p>
                        In order to find them, go to Posts and filter by Drafts; find the post you want and click Use;
                        select a social account on the left, and then hit Drafts - here's where you can also set up the
                        time and time this post is supposed to be published.
                    </p>
                </div>
            </div>
            <div class="faq-item">
                <div class="faq-link">
                    <h2>Can I go a specific date in a past/future fast?</h2>
                    <i class='bx bx-plus'></i>
                </div>
                <div class="faq-content">
                    <p>
                        Sure thing! On the virtual calendar, you can find a small calendar icon at the top filters'
                        menu. If you click it, you can select the desired date you want to be jumped at in a second.
                    </p>
                    <p>
                        Also, to get back where you were, simply hit the target icon next to it and {{ env('APP_NAME', 'Engagyo') }} will bring
                        you to the current date.
                    </p>
                </div>
            </div>
